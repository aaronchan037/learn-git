# 初始化一个 git 仓库
git init
# 克隆仓库
git clone <url> [name]


# 添加文件到缓存区
git add
# 提交
git commit
# 查看当前所在的分支
git branch
# 创建分支
git branch <name>
# 切换到某分支
# git checkout <name>
git switch <name>

# 合并分支
git merge
# 查看提交历史记录
git log
# 推送到远程仓库
git push



# 创建新的分支并切换到该分支上（相当于两条命令）
# git checkout -b <name>   ===  git branch <name> && git checkout <name>
git switch -c <name>








